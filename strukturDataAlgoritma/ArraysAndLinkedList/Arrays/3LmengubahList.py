warna = ["Merah", "Hijau", "Biru", "Kuning"]
print(f"List warna awal: {warna}")

# Mengubah elemen pada indeks 1 dari "Hijau" menjadi "Ungu"
warna[1] = "Ungu"
print(f"List warna setelah diubah: {warna}") # Output: ['Merah, 'Ungu', 'Biru', 'Kuning']

# Mengubah elemen terakhir
warna[-1] = "Oranye"
print(f"List warna setelah mengubah terakhir: {warna}") # Output: ['Merah', 'Ungu', 'Biru', 'Oranye']
